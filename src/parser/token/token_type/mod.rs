mod helper_methods;
#[derive(Debug, PartialEq, Eq, Clone, Copy)]
pub enum TokenType {
    TokenLeftParen,
    TokenRightParen,
    TokenLeftCurlyBrace,
    TokenRightCurlyBrace,
    TokenLeftSquareBracket,
    TokenRightSquareBracket,
    TokenDoubleQuote,
    TokenSemicolon,
    TokenBang,
    TokenAssign,
    TokenComma,
    TokenMinus,
    TokenPlus,
    TokenSlash,
    TokenStar,
    TokenEqualEqual,
    TokenBangEqual,
    TokenGreater,
    TokenGreaterEqual,
    TokenLess,
    TokenLessEqual,
    TokenNumber,
    TokenIdentifier,
    TokenString,
    TokenTrue,
    TokenFalse,
    TokenDefine,
    TokenMutable,
    TokenFunction,
    TokenTyping,
    TokenPrint,
    TokenReturn,
    TokenIf,
    TokenElse,
    TokenBreak,
    TokenContinue,
    TokenLoop,
    TokenError,
    TokenEOF,
}
impl From<TokenType> for usize {
    fn from(value: TokenType) -> Self {
        match value {
            TokenType::TokenLeftParen => 0,
            TokenType::TokenRightParen => 1,
            TokenType::TokenLeftCurlyBrace => 2,
            TokenType::TokenRightCurlyBrace => 3,
            TokenType::TokenLeftSquareBracket => 4,
            TokenType::TokenRightSquareBracket => 5,
            TokenType::TokenDoubleQuote => 6,
            TokenType::TokenSemicolon => 7,
            TokenType::TokenBang => 8,
            TokenType::TokenAssign => 9,
            TokenType::TokenComma => 10,
            TokenType::TokenMinus => 11,
            TokenType::TokenPlus => 12,
            TokenType::TokenSlash => 13,
            TokenType::TokenStar => 14,
            TokenType::TokenEqualEqual => 15,
            TokenType::TokenBangEqual => 16,
            TokenType::TokenGreater => 17,
            TokenType::TokenGreaterEqual => 18,
            TokenType::TokenLess => 19,
            TokenType::TokenLessEqual => 20,
            TokenType::TokenNumber => 21,
            TokenType::TokenIdentifier => 22,
            TokenType::TokenString => 23,
            TokenType::TokenTrue => 24,
            TokenType::TokenFalse => 25,
            TokenType::TokenDefine => 26,
            TokenType::TokenMutable => 27,
            TokenType::TokenFunction => 28,
            TokenType::TokenTyping => 29,
            TokenType::TokenPrint => 30,
            TokenType::TokenReturn => 31,
            TokenType::TokenIf => 32,
            TokenType::TokenElse => 33,
            TokenType::TokenBreak => 34,
            TokenType::TokenContinue => 35,
            TokenType::TokenLoop => 36,
            TokenType::TokenError => 37,
            TokenType::TokenEOF => 38,
        }
    }
}
